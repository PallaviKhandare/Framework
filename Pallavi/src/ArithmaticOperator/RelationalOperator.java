package ArithmaticOperator;

public class RelationalOperator {

	public static void main(String[] args) 
	{
		//Relational Operator --> >,<,>=,<=,==,!=
		
		int c=30;
		int d=20;
		
		System.out.println(c>d);//true
		System.out.println(c<d);//false
		System.out.println(c>=d);//true
		System.out.println(c<=d);//false
		System.out.println(c==d);//false
		System.out.println(c!=d);//true
		

	}

}
